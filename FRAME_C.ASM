GetColorParams proc

			mov ax, 0900h
			mov dx, offset BackClrText
			int 21h

BackClrErr:	call GetColor
			cmp cx, 7d
			jne BackClrErr

			mov di, offset BACKGROUND_CLR
			mov [di], ah

			mov ax, 0900h
			mov dx, offset BackClrText
			int 21h

FrameClrErr:call GetColor
			cmp cx, 7d
			jne FrameClrErr

			mov di, offset FRAME_COLOR
			mov [di], ah

			mov ax, 0900h
			mov dx, offset BackClrText
			int 21h

StrClrErr:	call GetColor
			cmp cx, 7d
			jne StrClrErr

			mov di, offset STRING_COLOR
			mov [di], ah

			ret
GetColorParams endp

;----------------------------------------------------------------------------------------
; Описание: (ClearConsol) Очищает консоль для дальнейшего корректного отображения
;
; Входные параметры: es = 0b800h (Указывает на текстовую видеопамять)
;
; Возвращаемое значение: --//--
;
; Ожидаемый результат: вся текстовая видеопамять заполнена пробелами на черном фоне
