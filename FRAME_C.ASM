;----------------------------------------------------------------------------------------
; Описание: (AnalyzeComArgs) Анализирует аргументы командной строки
;
; Входные параметры: --//--
;
; Возвращаемое значение: dx - длина самой длиной строки
;						 длины строк и указатели на их начало лежат в стеке
;						 si - количество строк
; Ожидаемый результат:
;
; Испорченные регистры: di - используется для перемещению по адресам аргументов командной строки
;						bx - используется для
;----------------------------------------------------------------------------------------
; ОСОБЕННОСТИ:
;
; Если пользователь вводит машина%%мотик то это отображается так:
;			_____________________
;		   |      машина	     |
;		   |       (-/-)         |
;		   | 	   мотик		 |
;		   |_____________________|
; Если пользователь вводит машина% то это отображается так:
;			_____________________
;		   |      машина	     |
;		   |       (-/-)         |
;		   |_____________________|
;
;~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
; РЕАЛИЗАЦИЯ НА СИ:
;
;push 082h;
;while(di <= ds: byte ptr [080h] + 80h)
;{
;	if(*di == '%')
;	{
;		if(bx > dx) dx = bx;
;
;		push bx;
;		bx = 0;
;
;		si++;
;		di++;
;
;		push di;
;	}
;	else
;	{
;		bx++;
;		di++;
;	}
;}
;if(bx > dx) dx = bx;
;push bx;
;~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
AnalyzeComArgs proc
				pop cx		; сохраняем адрес возврата

				push 082h

				mov di, 082h
				xor si, si
				xor bx, bx
				xor dx, dx
				xor ax, ax

				mov al, ds: byte ptr [080h]
				add ax, 080h

	NextSym: 	cmp di, ax
				ja LastStr
					cmp ds: byte ptr [di], '%'
					je NewStr
						inc bx
						inc di
						jmp NextSym

		NewStr:		cmp bx, dx
					jbe NotNewMax
						mov dx, bx
		NotNewMax:	push bx

					xor bx, bx

					inc si
					inc di

					push di
					jmp NextSym

	LastStr:	cmp bx, dx
				jbe NotChange
					mov dx, bx
	NotChange:  push bx

				push cx
				ret
AnalyzeComArgs endp

;----------------------------------------------------------------------------------------
; Описание: (CalcShift) высчитывает смещение от начала строки до того места, в котором
;		    если бы располагалась первая буква строчки, то вся строчка была бы посередине
;
; Входные параметры: dx - содержит длину максимальной строки аргументов командной строки
;
; Возвращаемое значение: bp - содержит нужное смещение
;
; Ожидаемый результат: --//--
;
; Испорченные регистры: --//--
;----------------------------------------------------------------------------------------
CalcShift proc
				; Вычислим смещение от начала строки текстовой видеопамяти (dx)
				xor bp, bp
				mov bp, dx 		  ; bp = dx
				and bp, 0fffeh	  ; bp = bp*2 (bp = (dx%2)*2 )
				neg bp			  ; bp = -bp  (bp = -(dx%2)*2 )
				add bp, 80d		  ; bp += 80  (bp = 80-(dx%2)*2 )

				ret
CalcShift endp

;------------------------------------------------------------------------------------------
; Описание: (SetBackground) Устанавливает фон для рамки
;
; Входные параметры: es = 0b800h (Указывает на текстовую видеопамять)
;					 dx - длина самой длиной строки
;					 si - количество строк
;					 bp - содержит смещение от начала строки видеопамяти
;						  до первого символа аргументов командной строки;
;
; Возвращаемое значение: --//--
;
; Ожидаемый результат: нужная часть видеопамяти заполнена пробелами цвета фона рамки
;
; Испорченные регистры: ax - вычисление смещения хранящегося в di
;						   - символ и его атрибут
;						bx - счетчик цикла
;						cx - счетчик для rep
;						di - смещение в видеопамяти
;------------------------------------------------------------------------------------------
;Число строк фона = si + 6  bx+10d-((si+1)//2)
;               si == 3:                         si == 2:                         si == 1:                         si == 0:
;       ________10d-((3+1)//2)           ________10d-((3+1)//2)           ________10d-((3+1)//2)           ________10d-((3+1)//2)
;      /                                /                                /                                /
;-->  8    ****************       -->  8                           -->  8                           -->  8
;-->  9    ****************       -->  9    ****************       -->  9    ****************       -->  9
;--> 10    ****~~~~~~~~****       --> 10    ****************       --> 10    ****************       --> 10    ****************
;--> 11    ****|_str1_|****       --> 11    ****~~~~~~~~****       --> 11    ****~~~~~~~~****       --> 11    ****************
;--> 12    ****|_str2_|****       --> 12    ****|_str1_|****       --> 12    ****|_str1_|****       --> 12    ****~~~~~~~~****
;--> 13    ****|_str3_|****       --> 13    ****|_str2_|****       --> 13    ****~~~~~~~~****       --> 13    ****~~~~~~~~****
;--> 14    ****~~~~~~~~****       --> 14    ****~~~~~~~~****       --> 14    ****************       --> 14    ****************
;--> 15    ****************       --> 15    ****************       --> 15    ****************       --> 15    ****************
;--> 16    ****************       --> 16    ****************       --> 16                           --> 16
;     \________ то на сколько надо умножить 160
SetBackground   proc
			sub bp, 8d ; обновим смещение именно для фона

			mov ax, si			; ax = si
			add ax, 1           ; ax = si+1
			shr ax, 1			; ax = (si+1)//2
			neg ax				; ax = -((si+1)//2)
			add ax, 10d			; ax = 10d-((si+1)//2)
			mov bx, 160d
			mul bl				; ax = 160d*(10d-((si+1)//2))
			add ax, bp			; ax = bp+160d*(10d-((si+1)//2))
			mov di, ax			; di = ax

			xor bx, bx
			add si, 4d ; установим количество строк для фона

			push bp
	Lines:		; Устанавливаем символ и атрибут
				mov bp, offset BACKGROUND_SYM
				mov al, [bp]
				mov bp, offset BACKGROUND_CLR
				mov ah, [bp]

				; cx = StrLength + 10 - количество повторов (длина фона рамки)
				mov cx, dx
				add cx, 8d

				rep stosw

				inc bx
				add di, 160d-8d*2d
				sub di, dx
				sub di, dx
			cmp bx, si
			jle Lines

			pop bp
			add bp, 8d ; возвращаем стандартное смещение
			sub si, 4d ; возвращаем обычное количество строк
			ret
SetBackground endp

