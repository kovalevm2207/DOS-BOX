;					    cx - используется как счетчик для rep
;					    di - используется для смещения в текстовой видеопамяти
;----------------------------------------------------------------------------------------
GetColor proc
			mov ax, 0a00h
			mov dx, offset ColorBuf
			int 21h

			xor ax, ax
			xor cx, cx
			sub cx, 1
			mov si, dx
			add si, 10d

	NextBit:cmp cx, 7d
			jge CorrectClr

				inc cx
				dec si
				mov dx, 1d

				mov al, [si]

				cmp al, '0'
				je NextBit
					cmp al, '1'
					jne WrongClr

					shl dx, cl
					or ah, dl

					jmp NextBit

WrongClr:   mov ah, 09h
			mov dx, offset WrongFormat
			int 21h

CorrectClr:	ret

GetColor endp

