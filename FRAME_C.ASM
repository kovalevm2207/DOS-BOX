GetParams proc

			call GetFrameSymParams
			call GetColorParams

		    ret
GetParams endp

GetFrameSymParams proc

			mov ax, 0900h
			mov dx, offset FrameText
			int 21h

FrameSym:	mov ax, 0a00h
			mov dx, offset FrameBuf
			int 21h

			mov bx, dx
			mov al, [bx+1d]
			cmp al, 9d
			je CorrectLen

				mov ax, 0900h
				mov dx, offset WrongFormat
				int 21h

				jmp FrameSym

CorrectLen: add bx, 2
			mov si, offset L_U_CORNER
			mov al, [bx]
			mov [si], al

			inc bx
			mov si, offset FRAME_T
			mov al, [bx]
			mov [si], al

			inc bx
			mov si, offset R_U_CORNER
			mov al, [bx]
			mov [si], al

			inc bx
			mov si, offset FRAME_L
			mov al, [bx]
			mov [si], al

			inc bx
			mov si, offset BACKGROUND_SYM
			mov al, [bx]
			mov [si], al

			inc bx
			mov si, offset FRAME_R
			mov al, [bx]
			mov [si], al

			inc bx
			mov si, offset L_D_CORNER
			mov al, [bx]
			mov [si], al

			inc bx
			mov si, offset FRAME_D
			mov al, [bx]
			mov [si], al

			inc bx
			mov si, offset R_D_CORNER
			mov al, [bx]
			mov [si], al

			ret
GetFrameSymParams endp

;----------------------------------------------------------------------------------------
; Описание: (ClearConsol) Очищает консоль для дальнейшего корректного отображения
;
; Входные параметры: es = 0b800h (Указывает на текстовую видеопамять)
