; Испорченные регистры: ax - символ и его атрибут
;						cx - счетчик для rep
;						di - смещение в видеопамяти
;----------------------------------------------------------------------------------------
DrawFrameDown proc
			push si

			; рисуем левый нижний угол
			add di, 160-3*2   			;| обновляем смещение
			sub di, dx					;|
			sub di, dx					;|

			mov si, offset L_D_CORNER
			mov al, [si]
			mov es: byte ptr [di],   al

			mov es: byte ptr [di+1], ah

			; рисуем нижнюю грань
			mov si, offset FRAME_D
			mov al, [si]     ;| установили символ

			;|mov ah, ah        и атрибут

			mov cx, 2 			;| количество повторений
			add cx, dx			;|

			add di, 2d			; обновляем смещение
			rep stosw

			; рисуем правый нижний угол
			mov si, offset R_D_CORNER
			mov al, [si]
			mov es: byte ptr [di],   al

			mov es: byte ptr [di+1], ah

			pop si
			ret
DrawFrameDown endp

;-----------------------------------------------------------------------------------------
; Описание: по центру экрана выводит строчку с соответствующим фоном
;
; Входные параметры: es = 0b800h (Указывает на текстовую видеопамять)
;					 si - количество строк
;
; Возвращаемое значение: --//--
;
; Ожидаемый результат: на середине экрана видна строчка с заданным фоном
;
; Испорченные регистры: --//-- в процессе
;-----------------------------------------------------------------------------------------
WriteStr proc
			pop cx ; сохранили адрес возврата

		    mov ax, si			   ;| ax = si
		    add ax, 1			   ;| ax = si+1
			shr ax, 1			   ;| ax = ((si+1)//2)
			neg ax				   ;| ax = -((si+1)//2)
			add ax, 11d			   ;| ax = 11d-((si+1)//2)    <-- сейчас это номер строки видеопамяти с верхней рамкой

			inc si
				; si - хранит номер текущей строки, в конце цикла мы его уменьшаем
				; тогда цикл продолжается до тех пор пока наше si не станет равным нулю
    NextStr:	pop dx ; длина строки
    			pop bp ; адрес начала строки

				cmp dx, 0d
				je Skip
					push ax

			    	add ax, si             ;| ax = 11d+si-((si+1)//2) <-- прибавив номер текущей строки получили нужный номер строки видеопамяти
		    		mov bx, 160d
					mul bl                 ;| ax = 160d*(11d+si-((si+1)//2))

			    	push bp				   ;| сохранили в стек адрес начала строки

			    	call CalcShift		   ;| положит в bp нужное значение смещения исходя из длины строки в dx
			    	add ax, bp             ;| устанавливаем смещение для первого символа строки

					pop bp                 ;| возвращаем из стека адрес начала строки

					mov di, ax             ;| кладем в тот регистр с помощью которого можно обращаться в память
					pop ax

			Next:	    mov bl, ds: byte ptr [bp] ; сохранили символ

			    	    mov es: byte ptr [di], bl
						mov bx, offset STRING_COLOR
						mov bl, [bx]
				        mov es: byte ptr [di+1], bl

            	        inc bp
			    		add di, 2d
			    		dec dx
			    	cmp dx, 0d
					ja Next

        Skip:   dec si
			cmp si, 0d
			ja NextStr

		    push cx   ; сохранили адрес возврата
		    ret
WriteStr	endp

