;~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
;|ПРО ХРАНЕНИЕ ДАННЫХ ОБ АРГУМЕНТАХ КОМАНДНОЙ СТРОКИ В ЭТОЙ ПРОГРАММЕ|
;~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
;
; 	длину каждой строки будем хранить в стеке, потому что мы не знаем сколько из должно быть
; и указатель наверное тоже будем там хранить, рядом с длиной командной строки
;   указатель1  }
;	длина1		}--- STACK
;	 ...        }
;   указательN  }
;   длинаN      } <--- SP
;
;~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
;|ПРО ХРАНЕНИЕ ПАРАМЕТРОВ ДЛЯ ОТРИСОВКИ РАМКИ |
;~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
;
;	будем поочередно запрашивать у пользователя из консоли в целях корректности
;работы программы, а именно такие параметры рамки как:
;
;    Необходимые цвета:
;	 ~~~~~~~~~~~~~~~~~~
;BACKGROUND_COLOR - цвет заднего символа рамки
;FRAME_COLOR - цвет рамки и ее фона
;STRING_COLOR - цвет строки
;
;	 Необходимые символы:
;    ~~~~~~~~~~~~~~~~~~~~
;BACKGROUND_SYM - из каких символов состоит задний фона
;
;L_U_CORNER - символ для левого верхнего угла    }
;FRAME_T - символ для верхнего ребра             }
;R_U_CORNER - символ для верхнего правого угла   }
;FRAME_L - символ для левого ребра               } запрашивается как одна строка
;FRAME_R - символ для правого ребра              }
;L_D_CORNER - символ для левого нижнего угла     }
;FRAME_D - символ для нижнего ребра              }
;R_D_CORNER - символ для нижнего правого угла    }
;
;~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~

Start:		; надо установить сегментный регистр для
			; записи в видеопамять (text)
			mov ax, 0b800h ; кладем сначала ax, потому что
						   ; нельзя сразу класть в es
			mov es, ax	   ; записываем значение в es

			cld				; сброс флага (DF = 0)
							; чтобы di++ & si++
							; антоним: std

			call GetArgs            ; проанализировали аргумент командной строки
            jc @@Err
            call GetStrings

			call ClearConsol  		; очистили консоль
			call DrawFrame			; нарисовали рамку
			call WriteStr			; вывели строчку

	 @@End: mov ax, 4c00h
			int 21h					; завершить программу

     @@Err: mov ah, 09h
            mov dx, offset ErrMsg
            int 21h
            jmp @@End

;----------------------------------------------------------------------------------------
; Описание: (ClearConsol) Очищает консоль для дальнейшего корректного отображения
;
; Входные параметры: es = 0b800h (Указывает на текстовую видеопамять)
;
; Возвращаемое значение: --//--
;
; Ожидаемое состояние: DF = 0
;
; Испорченные регистры: ax - используется для хранения символа и его атрибута
;					    cx - используется как счетчик для rep
;					    di - используется для смещения в текстовой видеопамяти
;----------------------------------------------------------------------------------------
;	  ________________ просто название функции
;	 /             ___ показывает начало функции
;   /             /		(можно опустить)
ClearConsol	proc
				mov al, ' '			; символ  |
				mov ah, 00000000b	; атрибут | что
				mov cx, 80d*25d		; сколько (для rep)
				mov di, 0h			; смещение от es

				rep stosw		;
				;\	   \____ выводит слово и di++ & si++
				; \_________ для  повтора cx раз
				ret
ClearConsol		endp ; строка показывает конец объявления
				     ; функции (можно опустить, но с ними
					 ; код более понятный)

;----------------------------------------------------------------------------------------
; Описание: (GetArgs) Анализирует аргументы командой строки
;
; Входные параметры: --//--
;
; Возвращаемое значение: в переменных в области .data заложены нужные значения
;						 di - указатель на первый символ текстовой строки
;
; Ожидаемое состояние: --//--
;
; Испорченные регистры: ax - для передачи параметров в области памяти
;					    di - используется для смещения по адресам символов аргументов командной строки
;----------------------------------------------------------------------------------------
;Используемый формат аргументов командной строки
;(в DOS все пробелы и табуляции сворачиваются до одного пробела)
;
;          81  82   85   88      90  92   95   <-- адрес начала каждой части в hex формате
;           |   |    |    |       |   |    |
;           v   v    v    v       v   v    v
;S:FRAME.COM    aa   bb   *-+\~/?_$   cc   %String% with%% <--  zero_strings%%
;               (1)  (2)     (3)      (4)                  (5)
;
;(1), (2), (4) - шестнадцатеричное представление цвета символа ии его фона
;(5) - строка, для рамки, где % - означает переход на новую строку
;(3) - символы для рамки, расположенные по следующему формату:
;(обратите внимание на пробелы рядом со строчками)
;
;               ~~~~~~~~~~~~~~~~~~~~~~~~~~~~
;               ~~~~~~~~~~~~~~~~~~~~~~~~~~~~
;               ~~~~*------------------+~~~~
;               ~~~~\~~~~~~~~~~~~~~~~~~/~~~~
;               ~~~~\~~~~~~~~~~~~~~~~~~/~~~~
;               ~~~~\~~~~~~String~~~~~~/~~~~
;               ~~~~\~~~~~~~~~~~~~~~~~~/~~~~
;               ~~~~\~~~~~~~ with~~~~~~/~~~~
;               ~~~~\~~~~~~~~~~~~~~~~~~/~~~~
;               ~~~~\ <-- zero_strings /~~~~
;               ~~~~\~~~~~~~~~~~~~~~~~~/~~~~
;               ~~~~\~~~~~~~~~~~~~~~~~~/~~~~
;               ~~~~?__________________$~~~~
;               ~~~~~~~~~~~~~~~~~~~~~~~~~~~~
;               ~~~~~~~~~~~~~~~~~~~~~~~~~~~~
;
;~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
GetArgs proc
            mov di, 082h

            call GetColor
            jc @@End
            call CheckSpace
            jc @@End
            mov BACKGROUND_CLR, ah

            call GetColor
            jc @@End
            call CheckSpace
            jc @@End
            mov FRAME_CLR, ah

			call GetFrameSym
            jc @@End
            call CheckSpace
            jc @@End

			call GetColor
            jc @@End
            call CheckSpace
            jc @@End
            mov STRING_CLR, ah

	  @@End:ret
GetArgs endp

;----------------------------------------------------------------------------------------
; Описание: (GetColor) считывает 2х значное hex-число и преобразовывает в цвет
;
; Входные параметры: di - указывает на место в памяти где первый символ
;
; Возвращаемое значение: ah - содержит значение 2х значного hex числа
;                        CF = 1, если была ошибка
; Ожидаемое состояние: ds - на соответствующий сегмент памяти
;					   CF = 0
;
; Испорченные регистры: al - промежуточные сравнения
;----------------------------------------------------------------------------------------
GetColor proc
        xor ax, ax
        xor cl, cl

 @@Next:mov al, ds: byte ptr [di]

        cmp al, '0'
        jb  @@Err
        cmp al, '9'
        jbe @@Num

        cmp al, 'A'
        jb  @@Err
        cmp al, 'F'
        jbe @@B_W

        cmp al, 'a'
        jb  @@Err
        cmp al, 'f'
        jbe @@L_W

  @@Err:stc
  @@End:ret

  @@Num:sub al, '0'
        shl al, cl
        add ah, al
        jmp @@Act

  @@B_W:sub al, 'A'
        add al, 0ah
        shl al, cl
        add ah, al
        jmp @@Act

  @@L_W:sub al, 'a'
        add al, 0ah
        shl al, cl
        add ah, al
        jmp @@Act

  @@Act:cmp cl, 4d
        jae @@End
        add cl, 4d
        inc di
        jmp @@Next

GetColor endp

;----------------------------------------------------------------------------------------
; Описание: (CheckSpace) проверяет наличие пробельных символов
;
; Входные параметры: di - указывает на место в памяти где первый символ
;
; Возвращаемое значение: di - указывает на первый не пробельный символ
;						 CF = 1, если была ошибка
; Ожидаемое состояние: ds - на соответствующий сегмент памяти
;					   CF = 0
;
; Испорченные регистры: --//--
;----------------------------------------------------------------------------------------
CheckSpace proc
            inc di
            cmp ds: byte ptr [di], ' '
            je @@Next
            stc
     @@Next:inc di
			cmp di, ' '
			je @@Next

            ret
CheckSpace endp

;----------------------------------------------------------------------------------------
; Описание: (GetFrameSym) считывает строчку из 9 символов, содержащие формат для рамки
;
; Входные параметры: di - указывает на место в памяти где начинается строка
;
; Возвращаемое значение: в памяти на соответствующих местах лежат символы из строки
;
; Ожидаемое состояние: ds - на соответствующий сегмент памяти
;
; Испорченные регистры: al - для передачи параметров
;----------------------------------------------------------------------------------------
GetFrameSym proc
			mov al, [di]
			mov L_U_CORNER, al

			inc di
			mov al, [di]
			mov FRAME_T, al

			inc di
			mov al, [di]
			mov R_U_CORNER, al

			inc di
			mov al, [di]
			mov FRAME_L, al

			inc di
			mov al, [di]
			mov BACKGROUND_SYM, al

			inc di
			mov al, [di]
			mov FRAME_R, al

			inc di
			mov al, [di]
			mov L_D_CORNER, al

			inc di
			mov al, [di]
			mov FRAME_D, al

			inc di
			mov al, [di]
			mov R_D_CORNER, al

			ret
GetFrameSym endp

;----------------------------------------------------------------------------------------
; Описание: (GetStrings) анализирует текстовую строку, переданную как аргумент командной строки
;
; Входные параметры: di - указатель на первый символ
;
; Возвращаемое значение: dx - длина самой длиной строки
;						 длины строк и указатели на их начало лежат в стеке
;						 si - количество строк
;
; Ожидаемое состояние: ds - на соответствующий сегмент памяти
;
; Испорченные регистры: di - используется для перемещению по адресам аргументов командной строки
;						bx - используется для
;	 					cx - для хранения адреса возврата
;						ax - временное хранение значений
;----------------------------------------------------------------------------------------
; ОСОБЕННОСТИ:
;
; Если пользователь вводит машина%%мотик то это отображается так:
;			_____________________
;		   |      машина	     |
;		   |       (-/-)         |
;		   | 	   мотик		 |
;		   |_____________________|
; Если пользователь вводит машина% то это отображается так:
;			_____________________
;		   |      машина	     |
;		   |       (-/-)         |
;		   |_____________________|
;
;~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
; РЕАЛИЗАЦИЯ НА СИ:
;
;push di;
;while(di <= ds: byte ptr [080h] + 80h)
;{
;	if(*di == '%')
;	{
;		if(bx > dx) dx = bx;
;
;		push bx;
;		bx = 0;
;
;		si++;
;		di++;
;
;		push di;
;	}
;	else
;	{
;		bx++;
;		di++;
;	}
;}
;if(bx > dx) dx = bx;
;push bx;
;~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
GetStrings proc
				pop cx		; сохраняем адрес возврата

				push di

				xor si, si
				xor bx, bx
				xor dx, dx
				xor ax, ax

				mov al, ds: byte ptr [080h]
				add ax, 080h

	NextSym: 	cmp di, ax
				ja LastStr
					cmp ds: byte ptr [di], '%'
					je NewStr
						inc bx
						inc di
						jmp NextSym

		NewStr:		cmp bx, dx
					jbe NotNewMax
						mov dx, bx
		NotNewMax:	push bx

					xor bx, bx

					inc si
					inc di

					push di
					jmp NextSym

	LastStr:	cmp bx, dx
				jbe NotChange
					mov dx, bx
	NotChange:  push bx

				push cx
				ret
GetStrings endp

;------------------------------------------------------------------------------------------
;Число строк фона = si + 6  bx+10d-((si+1)//2)
;               si == 3:                         si == 2:                         si == 1:                         si == 0:
;       ________10d-((3+1)//2)           ________10d-((3+1)//2)           ________10d-((3+1)//2)           ________10d-((3+1)//2)
;      /                                /                                /                                /
;-->  8    ****************       -->  8                           -->  8                           -->  8
;-->  9    ****************       -->  9    ****************       -->  9    ****************       -->  9
;--> 10    ****~~~~~~~~****       --> 10    ****************       --> 10    ****************       --> 10    ****************
;--> 11    ****|_str1_|****       --> 11    ****~~~~~~~~****       --> 11    ****~~~~~~~~****       --> 11    ****************
;--> 12    ****|_str2_|****       --> 12    ****|_str1_|****       --> 12    ****|_str1_|****       --> 12    ****~~~~~~~~****
;--> 13    ****|_str3_|****       --> 13    ****|_str2_|****       --> 13    ****~~~~~~~~****       --> 13    ****~~~~~~~~****
;--> 14    ****~~~~~~~~****       --> 14    ****~~~~~~~~****       --> 14    ****************       --> 14    ****************
;--> 15    ****************       --> 15    ****************       --> 15    ****************       --> 15    ****************
;--> 16    ****************       --> 16    ****************       --> 16                           --> 16
;     \________ то на сколько надо умножить 160
;----------------------------------------------------------------------------------------
; Описание: (DrawFrame) рисует рамку на нужном месте в видеопамяти
; Входные параметры: es = 0b800h (Указывает на текстовую видеопамять)
;					 dx - содержит длину максимальной строки аргументов командной строки
;					 si - количество строк
;
; Возвращаемое значение: --//--
;
; Ожидаемое состояние: CF = 0
;
; Испорченные регистры: ax, cx, di, bx
;----------------------------------------------------------------------------------------
DrawFrame proc
			mov di, dx
			and di, 0fffeh
			neg di
			add di, 72d

			mov ax, si
			inc ax
			shr ax, 1
			neg ax
			add ax, 10d
			mov bl, 160d
			mul bl
			add di, ax 	      ; 72-(dx//2)*2+160d*(10d-((si+1)//2)

			push di
			mov al, BACKGROUND_SYM
			mov ah, BACKGROUND_CLR
			mov cx, dx
			add cx, 8
			rep stosw
			pop di

			xor bx, bx
    	@@Next: inc bx
				add di, 160d
				push di
				mov cx, 2
				rep stosw

				add dx, 2
				cmp bx, 1
				je @@TOP1

					cmp bx, dx
					je @@DOWN1

						mov al, FRAME_L
						jmp @@CNT1

		   @@DOWN1: mov al, L_D_CORNER
					jmp @@CNT1

		@@TOP1: mov al, L_U_CORNER

		@@CNT1: sub dx, 2
				mov ah, FRAME_CLR
				mov es: word ptr [di], ax

				add di, 2

				add dx, 2
				cmp bx, 1
				je @@TOP2

					cmp bx, dx
					je @@DOWN2

						mov al, BACKGROUND_SYM
						mov ah, BACKGROUND_CLR
						jmp @@CNT2

		   @@DOWN2: mov al, FRAME_D
					jmp @@CNT2

	    @@TOP2: mov al, FRAME_T
				jmp @@CNT2

	 @@SubNext: jmp @@Next

		@@CNT2: sub dx, 2
		 		mov cx, dx
				add cx, 2
				rep stosw

				add dx, 2
				cmp bx, 1
				je @@TOP3

					cmp bx, dx
					je @@DOWN3

						mov al, FRAME_R
						jmp @@CNT3

		   @@DOWN3: mov al, R_D_CORNER
					jmp @@CNT3

	    @@TOP3: mov al, R_U_CORNER

		@@CNT3: sub dx, 2
				mov ah, FRAME_CLR
				mov es: word ptr [di], ax

				add di, 2
				mov al, BACKGROUND_SYM
				mov ah, BACKGROUND_CLR
				mov cx, 2
				rep stosw

				pop di
				add dx, 2
				cmp bx, dx
				pushf
				sub dx, 2
				popf
				jb @@SubNext

			add di, 160d
			mov al, BACKGROUND_SYM
			mov ah, BACKGROUND_CLR
			mov cx, dx
			add cx, 8
			rep stosw

			ret
DrawFrame endp

;-----------------------------------------------------------------------------------------
; Описание: по центру экрана выводит строчку с соответствующим фоном
;
; Входные параметры: es = 0b800h (Указывает на текстовую видеопамять)
;					 si - количество строк
;
; Возвращаемое значение: --//--
;
; Ожидаемое состояние: на середине экрана видна строчка с заданным фоном
;
; Испорченные регистры: --//-- в процессе
;-----------------------------------------------------------------------------------------
WriteStr proc
			pop cx ; сохранили адрес возврата

		    mov ax, si			   ;| ax = si
		    add ax, 1			   ;| ax = si+1
			shr ax, 1			   ;| ax = ((si+1)//2)
			neg ax				   ;| ax = -((si+1)//2)
			add ax, 11d			   ;| ax = 11d-((si+1)//2)    <-- сейчас это номер строки видеопамяти с верхней рамкой

			inc si
				; si - хранит номер текущей строки, в конце цикла мы его уменьшаем
				; тогда цикл продолжается до тех пор пока наше si не станет равным нулю
  @@NextStr:	pop dx ; длина строки
    			pop bp ; адрес начала строки

				cmp dx, 0d
				je @@Skip
					push ax

			    	add ax, si             ;| ax = 11d+si-((si+1)//2) <-- прибавив номер текущей строки получили нужный номер строки видеопамяти
		    		mov bx, 160d
					mul bl                 ;| ax = 160d*(11d+si-((si+1)//2))

			    	push bp				   ;| сохранили в стек адрес начала строки

					xor bp, bp
					mov bp, dx
					and bp, 0fffeh
					neg bp
					add bp, 80d		  	   ; 80-(dx%2)*2 )
			    	add ax, bp             ;| устанавливаем смещение для первого символа строки

					pop bp                 ;| возвращаем из стека адрес начала строки

					mov di, ax             ;| кладем в тот регистр с помощью которого можно обращаться в память
					pop ax

		  @@Next:	    mov bl, ds: byte ptr [bp] ; сохранили символ

			    	    mov es: byte ptr [di], bl
						mov bx, offset STRING_CLR
						mov bl, [bx]
        mov es: byte ptr [di+1], bl

            	        inc bp
			    		add di, 2d
			    		dec dx
			    	cmp dx, 0d
					ja @@Next

      @@Skip:   dec si
			cmp si, 0d
			ja @@NextStr

		    push cx   ; сохранили адрес возврата
		    ret
WriteStr	endp

.data

